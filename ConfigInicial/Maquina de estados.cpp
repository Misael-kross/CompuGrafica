void DoMovement()
{
	if (keys[GLFW_KEY_W] || keys[GLFW_KEY_UP])
	{
		camera.ProcessKeyboard(FORWARD, deltaTime);
	}

	if (keys[GLFW_KEY_S] || keys[GLFW_KEY_DOWN])
	{
		camera.ProcessKeyboard(BACKWARD, deltaTime);
	}

	if (keys[GLFW_KEY_A] || keys[GLFW_KEY_LEFT])
	{
		camera.ProcessKeyboard(LEFT, deltaTime);
	}

	if (keys[GLFW_KEY_D] || keys[GLFW_KEY_RIGHT])
	{
		camera.ProcessKeyboard(RIGHT, deltaTime);
	}

	if (keys[GLFW_KEY_T])
	{
		pointLightPositions[0].x += 0.01f;
	}

	if (keys[GLFW_KEY_G])
	{
		pointLightPositions[0].x -= 0.01f;
	}

	if (keys[GLFW_KEY_Y])
	{
		pointLightPositions[0].y += 0.01f;
	}

	if (keys[GLFW_KEY_H])
	{
		pointLightPositions[0].y -= 0.01f;
	}

	if (keys[GLFW_KEY_U])
	{
		pointLightPositions[0].z -= 0.1f;
	}

	if (keys[GLFW_KEY_J])
	{
		pointLightPositions[0].z += 0.01f;
	}
}

void KeyCallback(GLFWwindow* window, int key, int scancode, int action, int mode)
{
	if (GLFW_KEY_ESCAPE == key && GLFW_PRESS == action)
	{
		glfwSetWindowShouldClose(window, GL_TRUE);
	}

	if (key >= 0 && key < 1024)
	{
		if (action == GLFW_PRESS)
		{
			keys[key] = true;
		}
		else if (action == GLFW_RELEASE)
		{
			keys[key] = false;
		}
	}

	if (keys[GLFW_KEY_SPACE])
	{
		active = !active;

		if (active)
		{
			Light1 = glm::vec3(0.2f, 0.8f, 1.0f);
		}
		else
		{
			Light1 = glm::vec3(0);
		}
	}

	if (keys[GLFW_KEY_N])
	{
		AnimBall = !AnimBall;
	}

	if (keys[GLFW_KEY_B])
	{
		dogAnim = 1;
		dogState = 0;
	}
}

void Animation()
{
	if (AnimBall)
	{
		rotBall += 0.9f;
	}

	if (dogAnim == 1)
	{
		// Movimiento de patas, cabeza y cola
		if (!step)
		{
			FLegs += 30.0f * deltaTime;
			RLegs += 30.0f * deltaTime;
			head += 30.0f * deltaTime;
			tail += 30.0f * deltaTime;

			if (RLegs >= 15.0f)
				step = true;
		}
		else
		{
			FLegs -= 30.0f * deltaTime;
			RLegs -= 30.0f * deltaTime;
			head -= 30.0f * deltaTime;
			tail -= 30.0f * deltaTime;

			if (RLegs <= -15.0f)
				step = false;
		}

		switch (dogState)
		{
		case 0:
			// Avanza hacia el frente
			dogRot = 0.0f;
			dogPos.z += dogSpeed * deltaTime;

			if (dogPos.z >= limiteFrente)
			{
				dogPos.z = limiteFrente;
				dogState = 1;
			}
			break;

		case 1:
			// Gira hacia la derecha
			dogRot += dogTurnSpeed * deltaTime;

			if (dogRot >= 90.0f)
			{
				dogRot = 90.0f;
				dogState = 2;
			}
			break;

		case 2:
			// Avanza hacia la derecha
			dogPos.x += dogSpeed * deltaTime;

			if (dogPos.x >= limiteDerecha)
			{
				dogPos.x = limiteDerecha;
				dogState = 3;
			}
			break;

		case 3:
			// Gira hacia atrás
			dogRot += dogTurnSpeed * deltaTime;

			if (dogRot >= 180.0f)
			{
				dogRot = 180.0f;
				dogState = 4;
			}
			break;

		case 4:
			// Avanza hacia atrás
			dogPos.z -= dogSpeed * deltaTime;

			if (dogPos.z <= limiteAtras)
			{
				dogPos.z = limiteAtras;
				dogState = 5;
			}
			break;

		case 5:
			// Gira hacia la izquierda
			dogRot += dogTurnSpeedSlow * deltaTime;

			if (dogRot >= 270.0f)
			{
				dogRot = 270.0f;
				dogState = 6;
			}
			break;

		case 6:
			// Avanza hacia la izquierda
			dogPos.x -= dogSpeed * deltaTime;

			if (dogPos.x <= limiteIzquierda)
			{
				dogPos.x = limiteIzquierda;
				dogState = 7;
			}
			break;

		case 7:
		{
			// Avanza en línea recta hacia el origen
			glm::vec3 destino(0.0f, 0.0f, 0.0f);
			glm::vec3 direccion = destino - dogPos;

			if (glm::length(direccion) > 0.05f)
			{
				direccion = glm::normalize(direccion);
				dogPos += direccion * dogSpeed * deltaTime;

				dogRot = glm::degrees(atan2(direccion.x, direccion.z));
			}
			else
			{
				dogPos = destino;
				dogAnim = 0;
				dogState = 0;

				FLegs = 0.0f;
				RLegs = 0.0f;
				head = 0.0f;
				tail = 0.0f;
			}
			break;
		}
		}
	}
}

